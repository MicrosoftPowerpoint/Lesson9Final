    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed

        employee temp;
        String nm, type;
        int hours;
        double rate;
        try{
            nm = txtname.getText();
            hours = Integer.parseInt(txthours.getText());
            rate = Double.parseDouble(txtrate.getText());
            type = buttonGroup2.getSelection().getActionCommand();
        }catch(Exception e)
 
        {
        
        JOptionPane.showMessageDialog(this, " Must fill out form correctly");
        return;
        
        }
        System.out.println(type);
        if (type.equals("FT"))
            temp = new fulltimeemployee();
        
        else temp = new parttimeemployee();
        String choice = buttonGroup2.getSelection().getActionCommand();
    
        if(temp.setName(nm) && temp.setHours(hours) && temp.setRate (rate))
        {
            emp[size] = temp;
            tblemp.setValueAt(temp.getName(), size, 0);
            tblemp.setValueAt(nf.format(temp.getPay()), size, 1);
            size++;
            txttotal.setText(nf.format(employee.getTotalPay()));
          clearform();
        return;

        }
tblemp.setValueAt(choice, 0, 1);
tblemp.setValueAt("You Chose: ", 0, 0);
    }//GEN-LAST:event_btnaddActionPerformed

